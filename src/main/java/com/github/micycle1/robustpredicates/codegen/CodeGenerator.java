package com.github.micycle1.robustpredicates.codegen;

import com.github.micycle1.robustpredicates.errorbound.ErrorBoundDeriver;
import com.github.micycle1.robustpredicates.expansion.ExpansionEvaluator;
import com.github.micycle1.robustpredicates.expr.EvaluationPlan;
import com.github.micycle1.robustpredicates.expr.Expression;
import com.github.micycle1.robustpredicates.expr.Expressions;
import com.github.micycle1.robustpredicates.expr.Op;

import java.util.IdentityHashMap;
import java.util.List;
import java.util.Map;

/**
 * Emits the flat, Shewchuk-like adaptive predicate routines as Java source.
 *
 * <p>For every {@link PredicateSpec} three straight-line stage methods are
 * generated from the same construction-time plans that the interpreted stages
 * use, so generated and interpreted results agree bit for bit:
 * <ul>
 *   <li><b>stage A</b> — the semi-static filter: one local per plan slot,
 *       derived error-bound constants baked in as hex double literals, fully
 *       self-contained;</li>
 *   <li><b>stage B</b> — exact-difference tail checks with early
 *       {@code SIGN_UNCERTAIN} returns, then reduced-size expansion evaluation
 *       via {@link com.github.micycle1.robustpredicates.expansion.ExpansionArithmetic};</li>
 *   <li><b>stage D</b> — full expansion evaluation, always exact;</li>
 * </ul>
 * plus a public driver that chains them.
 *
 * <p>Output is deterministic: plan ordering follows the expression build
 * order and doubles are always formatted as hex literals
 * ({@link Double#toHexString}), never decimal.
 */
public final class CodeGenerator {

    private CodeGenerator() {
    }

    /** The canonical generated source for the four classic predicates. */
    public static String generateDefault() {
        List<PredicateSpec> specs = List.of(
                new PredicateSpec("orient2d", Expressions.ORIENT2D,
                        List.of("ax", "ay", "bx", "by", "cx", "cy"),
                        "Positive iff {@code c} lies to the left of the directed line"
                                + " {@code a -> b} (counterclockwise {@code abc})."),
                new PredicateSpec("orient3d", Expressions.ORIENT3D,
                        List.of("px", "py", "pz", "qx", "qy", "qz",
                                "rx", "ry", "rz", "sx", "sy", "sz"),
                        "Sign of {@code det[q-p, r-p, s-p]} (columns)."),
                new PredicateSpec("incircle", Expressions.INCIRCLE,
                        List.of("ax", "ay", "bx", "by", "cx", "cy", "dx", "dy"),
                        "Positive iff {@code d} lies inside the circle through"
                                + " {@code a, b, c} (counterclockwise {@code abc})."),
                new PredicateSpec("insphere", Expressions.INSPHERE,
                        List.of("px", "py", "pz", "qx", "qy", "qz",
                                "rx", "ry", "rz", "sx", "sy", "sz", "tx", "ty", "tz"),
                        "Sign of the lifted 4x4 insphere determinant with row order"
                                + " {@code p, r, q, s} (CGAL side_of_oriented_sphere"
                                + " convention)."));
        return generateClass("com.github.micycle1.robustpredicates.generated", "RobustPredicates", specs);
    }

    public static String generateClass(String packageName, String className,
                                       List<PredicateSpec> specs) {
        StringBuilder sb = new StringBuilder();
        sb.append("// GENERATED FILE - DO NOT EDIT.\n");
        sb.append("// Generated by com.github.micycle1.robustpredicates.codegen.GenerateMain from the expression trees in\n");
        sb.append("// com.github.micycle1.robustpredicates.expr.Expressions. Regenerate with: mvn -Pcodegen compile exec:java\n");
        sb.append("package ").append(packageName).append(";\n\n");
        sb.append("import com.github.micycle1.robustpredicates.expansion.ExpansionArithmetic;\n\n");
        sb.append("/**\n");
        sb.append(" * Shewchuk-like adaptive robust geometric predicates, generated from the\n");
        sb.append(" * expression-tree framework: each public method runs a semi-static\n");
        sb.append(" * floating-point filter (stage A), an exact-difference expansion stage\n");
        sb.append(" * (stage B) and an exact expansion-arithmetic fallback (stage D), so the\n");
        sb.append(" * returned sign is always the exact sign of the predicate, provided no\n");
        sb.append(" * intermediate product over- or underflows (the same domain restriction\n");
        sb.append(" * as Shewchuk's original routines).\n");
        sb.append(" */\n");
        sb.append("public final class ").append(className).append(" {\n\n");
        sb.append("    /** Internal sentinel: a filter stage could not certify the sign. */\n");
        sb.append("    public static final int SIGN_UNCERTAIN = -2;\n\n");
        sb.append("    private ").append(className).append("() {\n    }\n");
        for (PredicateSpec spec : specs) {
            emitDriver(sb, spec);
            emitStageA(sb, spec);
            emitExpansionStage(sb, spec, true);
            emitExpansionStage(sb, spec, false);
        }
        sb.append("}\n");
        return sb.toString();
    }

    // ------------------------------------------------------------------
    // Driver
    // ------------------------------------------------------------------

    private static void emitDriver(StringBuilder sb, PredicateSpec spec) {
        String params = String.join(", ",
                spec.paramNames().stream().map(p -> "double " + p).toList());
        String args = String.join(", ", spec.paramNames());
        sb.append('\n');
        sb.append("    /** ").append(spec.javadoc()).append(" */\n");
        sb.append("    public static int ").append(spec.name()).append('(').append(params)
                .append(") {\n");
        sb.append("        int s = ").append(spec.name()).append("StageA(").append(args)
                .append(");\n");
        sb.append("        if (s != SIGN_UNCERTAIN) {\n            return s;\n        }\n");
        sb.append("        s = ").append(spec.name()).append("StageB(").append(args)
                .append(");\n");
        sb.append("        if (s != SIGN_UNCERTAIN) {\n            return s;\n        }\n");
        sb.append("        return ").append(spec.name()).append("StageD(").append(args)
                .append(");\n");
        sb.append("    }\n");
    }

    // ------------------------------------------------------------------
    // Stage A
    // ------------------------------------------------------------------

    private static void emitStageA(StringBuilder sb, PredicateSpec spec) {
        Expression det = spec.expression();
        Expression err = new ErrorBoundDeriver().deriveErrorExpression(det);
        EvaluationPlan plan = EvaluationPlan.of(det, err);
        String params = String.join(", ",
                spec.paramNames().stream().map(p -> "double " + p).toList());
        sb.append('\n');
        sb.append("    static int ").append(spec.name()).append("StageA(").append(params)
                .append(") {\n");
        List<Expression> nodes = plan.nodes();
        for (int i = 0; i < nodes.size(); i++) {
            Expression node = nodes.get(i);
            if (node.isLeaf()) {
                continue;
            }
            sb.append("        final double v").append(i).append(" = ")
                    .append(scalarExpr(node, plan, spec)).append(";\n");
        }
        String detRef = "v" + plan.rootSlot(0);
        String errRef = "v" + plan.rootSlot(1);
        sb.append("        if (").append(detRef).append(" > ").append(errRef)
                .append(") {\n            return 1;\n        }\n");
        sb.append("        if (").append(detRef).append(" < -").append(errRef)
                .append(") {\n            return -1;\n        }\n");
        sb.append("        if (").append(errRef).append(" == 0.0 && ").append(detRef)
                .append(" == 0.0) {\n            return 0;\n        }\n");
        sb.append("        return SIGN_UNCERTAIN;\n");
        sb.append("    }\n");
    }

    private static String scalarExpr(Expression node, EvaluationPlan plan, PredicateSpec spec) {
        return switch (node.op()) {
            case ABS -> "Math.abs(" + scalarRef(node.left(), plan, spec) + ")";
            case SUM -> scalarRef(node.left(), plan, spec) + " + "
                    + scalarRef(node.right(), plan, spec);
            case DIFFERENCE -> scalarRef(node.left(), plan, spec) + " - "
                    + scalarRef(node.right(), plan, spec);
            case PRODUCT -> scalarRef(node.left(), plan, spec) + " * "
                    + scalarRef(node.right(), plan, spec);
            case MAX -> "Math.max(" + scalarRef(node.left(), plan, spec) + ", "
                    + scalarRef(node.right(), plan, spec) + ")";
            case MIN -> "Math.min(" + scalarRef(node.left(), plan, spec) + ", "
                    + scalarRef(node.right(), plan, spec) + ")";
            default -> throw new IllegalArgumentException("leaf has no expression: " + node);
        };
    }

    private static String scalarRef(Expression node, EvaluationPlan plan, PredicateSpec spec) {
        return switch (node.op()) {
            case ARGUMENT -> spec.paramNames().get(node.argN() - 1);
            case CONSTANT -> hexLiteral(node.value());
            default -> "v" + plan.slotOf(node);
        };
    }

    private static String hexLiteral(double v) {
        return Double.toHexString(v);
    }

    // ------------------------------------------------------------------
    // Stages B and D
    // ------------------------------------------------------------------

    private static void emitExpansionStage(StringBuilder sb, PredicateSpec spec,
                                           boolean stageB) {
        ExpansionEvaluator.Plan plan =
                new ExpansionEvaluator(spec.expression(), stageB).plan();
        Map<Expression, Integer> index = new IdentityHashMap<>();
        for (int i = 0; i < plan.evals().size(); i++) {
            index.put(plan.evals().get(i), i);
        }
        String params = String.join(", ",
                spec.paramNames().stream().map(p -> "double " + p).toList());
        sb.append('\n');
        sb.append("    static int ").append(spec.name()).append(stageB ? "StageB(" : "StageD(")
                .append(params).append(") {\n");
        sb.append("        final double[] buf = new double[").append(plan.totalSize())
                .append("];\n");

        if (stageB) {
            for (int i = 0; i < plan.evals().size(); i++) {
                if (!plan.leafDiff()[i]) {
                    continue;
                }
                Expression node = plan.evals().get(i);
                String l = leafRef(node.left(), spec);
                String r = leafRef(node.right(), spec);
                sb.append("        final double d").append(i).append(" = ").append(l)
                        .append(" - ").append(r).append(";\n");
                sb.append("        if (ExpansionArithmetic.twoDiffTail(").append(l)
                        .append(", ").append(r).append(", d").append(i)
                        .append(") != 0.0) {\n            return SIGN_UNCERTAIN;\n        }\n");
                sb.append("        buf[").append(plan.starts()[i]).append("] = d").append(i)
                        .append(";\n");
            }
        }

        for (int i = 0; i < plan.evals().size(); i++) {
            if (stageB && plan.leafDiff()[i]) {
                continue;
            }
            Expression node = plan.evals().get(i);
            sb.append("        final int e").append(i).append(" = ")
                    .append(expansionCall(node, i, plan, index, spec, stageB))
                    .append(";\n");
        }
        sb.append("        return ExpansionArithmetic.signOf(buf, ")
                .append(plan.starts()[plan.rootIndex()]).append(", e")
                .append(plan.rootIndex()).append(");\n");
        sb.append("    }\n");
    }

    private static String expansionCall(Expression node, int i, ExpansionEvaluator.Plan plan,
                                        Map<Expression, Integer> index, PredicateSpec spec,
                                        boolean stageB) {
        Expression left = node.left();
        Expression right = node.right();
        boolean leftLeaf = left.isLeaf();
        boolean rightLeaf = right.isLeaf();
        String start = String.valueOf(plan.starts()[i]);
        String ze = String.valueOf(plan.ze()[i]);
        switch (node.op()) {
            case SUM, DIFFERENCE -> {
                boolean minus = node.op() == Op.DIFFERENCE;
                if (leftLeaf && rightLeaf) {
                    return "ExpansionArithmetic." + (minus ? "twoDiff" : "twoSum") + "("
                            + leafRef(left, spec) + ", " + leafRef(right, spec)
                            + ", buf, " + start + ", " + ze + ")";
                }
                if (leftLeaf) {
                    return "ExpansionArithmetic.growExpansion(" + range(right, plan, index, stageB)
                            + ", " + leafRef(left, spec) + ", buf, " + start + ", " + minus
                            + ", false, " + ze + ")";
                }
                if (rightLeaf) {
                    return "ExpansionArithmetic.growExpansion(" + range(left, plan, index, stageB)
                            + ", " + leafRef(right, spec) + ", buf, " + start + ", false, "
                            + minus + ", " + ze + ")";
                }
                int li = index.get(left);
                int ri = index.get(right);
                boolean fast = plan.sizes()[li] > 2 && plan.sizes()[ri] > 2;
                return "ExpansionArithmetic." + (fast ? "fastExpansionSum" : "expansionSum") + "("
                        + range(left, plan, index, stageB) + ", "
                        + range(right, plan, index, stageB)
                        + ", buf, " + start + ", false, " + minus + ", " + ze + ")";
            }
            case PRODUCT -> {
                if (leftLeaf && rightLeaf) {
                    return "ExpansionArithmetic.twoProduct(" + leafRef(left, spec) + ", "
                            + leafRef(right, spec) + ", buf, " + start + ", " + ze + ")";
                }
                if (leftLeaf) {
                    return "ExpansionArithmetic.scaleExpansion("
                            + range(right, plan, index, stageB) + ", " + leafRef(left, spec)
                            + ", buf, " + start + ", " + ze + ")";
                }
                if (rightLeaf) {
                    return "ExpansionArithmetic.scaleExpansion("
                            + range(left, plan, index, stageB) + ", " + leafRef(right, spec)
                            + ", buf, " + start + ", " + ze + ")";
                }
                if (left == right && plan.sizes()[index.get(left)] == 2) {
                    return "ExpansionArithmetic.twoSquare(" + range(left, plan, index, stageB)
                            + ", buf, " + start + ", " + ze + ")";
                }
                return "ExpansionArithmetic.expansionTimes(" + range(left, plan, index, stageB)
                        + ", " + range(right, plan, index, stageB) + ", buf, " + start + ", "
                        + ze + ")";
            }
            default -> throw new IllegalArgumentException(
                    "unsupported operator in exact expression: " + node.op());
        }
    }

    /** {@code buf, start, end} triple for a child expansion. */
    private static String range(Expression child, ExpansionEvaluator.Plan plan,
                                Map<Expression, Integer> index, boolean stageB) {
        int ci = index.get(child);
        int start = plan.starts()[ci];
        String end = (stageB && plan.leafDiff()[ci])
                ? String.valueOf(start + 1)
                : "e" + ci;
        return "buf, " + start + ", " + end;
    }

    private static String leafRef(Expression leaf, PredicateSpec spec) {
        return leaf.op() == Op.ARGUMENT
                ? spec.paramNames().get(leaf.argN() - 1)
                : hexLiteral(leaf.value());
    }
}
